/*An array in JavaScript is a data structure that allows you to store multiple values in a single variable.
 Arrays can hold items of any type, such as numbers, strings, or even other arrays, and are indexed starting
  from 0. You can access and modify elements in an array using their index, and arrays come with various built-in 
  methods for manipulating the data they contain.*/

  /*Example 1: Creating and Accessing an Array*/
  let fruits = ["apple", "banana", "cherry"];
console.log(fruits[0]);  // Output: "apple"
console.log(fruits[2]);  // Output: "cherry"

/*n this example, we create an array fruits containing three string elements. The first element is accessed using
fruits[0], and the third element is accessed using fruits[2].*/


/*Example 2: Array Methods*/
let numbers = [1, 2, 3, 4];
numbers.push(5);  // Adds 5 to the end of the array
console.log(numbers);  // Output: [1, 2, 3, 4, 5]

/*Here, the push method is used to add a new element (5) to the end of the numbers array. JavaScript arrays come 
with a variety of methods like push, pop, shift, and unshift to manipulate their contents.

Arrays are a versatile and essential part of JavaScript, allowing for efficient storage and manipulation of
collections of data.*/

